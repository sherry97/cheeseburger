class Cheeseburger():
	calories = 0
	content = []

	def __init__():
		self.calories = calories
		self.content = content

	def calories():
		return calories

	def add(o):
		content[-1] = o
		calories += o.calories()

	def remove(o):
		content.remove(o);
		calories -= o.calories()

	def display():
		print(content)
